const hasChinese = (value: string) => /[\u4e00-\u9fff]/.test(value)

export function translateText(value: string, language: Language): string {
  if (language === 'zh-CN' || !value || !hasChinese(value)) return value
  const leading = value.match(/^\s*/)?.[0] || ''
  const trailing = value.match(/\s*$/)?.[0] || ''
  const core = value.trim().replace(/\s+/g, ' ')
  const exact = en[core]
  if (exact) return `${leading}${exact}${trailing}`
  for (const [pattern, replacer] of rules) {
    const match = core.match(pattern)
    if (match) return `${leading}${replacer(match)}${trailing}`
  }
  return value
}

export function formatLocaleDate(value: string | number | Date, language: Language, options?: Intl.DateTimeFormatOptions) {
  return new Date(value).toLocaleDateString(language === 'en' ? 'en-US' : 'zh-CN', options)
}

export function formatLocaleDateTime(value: string | number | Date, language: Language, options?: Intl.DateTimeFormatOptions) {
  return new Date(value).toLocaleString(language === 'en' ? 'en-US' : 'zh-CN', options)
}

export function isChineseText(value: string) {
  return hasChinese(value)
}
